SELECT DISTINCT N'銀行支店マスタ' AS NAME FROM M_BANK_SHITEN WHERE BANK_CD IN /*BANK_CD*/('') AND DELETE_FLG = 'False'
UNION
SELECT DISTINCT N'自社情報マスタ' AS NAME FROM M_CORP_INFO WHERE BANK_CD IN /*BANK_CD*/('') AND DELETE_FLG = 'False'
UNION
SELECT DISTINCT N'取引先マスタ' AS NAME 
FROM M_TORIHIKISAKI AS MT
	LEFT JOIN M_TORIHIKISAKI_SEIKYUU AS MT_SEI
	ON MT.TORIHIKISAKI_CD = MT_SEI.TORIHIKISAKI_CD
	AND MT.DELETE_FLG = 'False'
WHERE 
	MT_SEI.FURIKOMI_BANK_CD IN /*BANK_CD*/('')
UNION
SELECT DISTINCT N'引合取引先マスタ' AS NAME 
FROM M_HIKIAI_TORIHIKISAKI AS MHT
	LEFT JOIN M_HIKIAI_TORIHIKISAKI_SEIKYUU AS MHT_SEI
	ON MHT.TORIHIKISAKI_CD = MHT_SEI.TORIHIKISAKI_CD
	AND MHT.DELETE_FLG = 'False'
WHERE 
	MHT_SEI.FURIKOMI_BANK_CD IN /*BANK_CD*/('')