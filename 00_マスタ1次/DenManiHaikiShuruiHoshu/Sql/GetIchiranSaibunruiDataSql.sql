FROM

	((
		SELECT
			S.EDI_MEMBER_ID,
			S.HAIKI_SHURUI_CD,
			S.HAIKI_SHURUI_SAIBUNRUI_CD,
			S.HAIKI_SHURUI_NAME,
			S.HAIKI_BUNRUI,
