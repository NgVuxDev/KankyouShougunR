			S.KONGOU_KBN,
			S.HOUKOKUSHO_BUNRUI_CD,
			S.CREATE_USER,
			S.CREATE_DATE,
			S.CREATE_PC,
			S.UPDATE_USER,
			S.UPDATE_DATE,
			S.UPDATE_PC,
			S.DELETE_FLG,
			S.TIME_STAMP,
			ISNULL(HB.HOUKOKUSHO_BUNRUI_NAME_RYAKU,N'') AS HOUKOKUSHO_BUNRUI_NAME_RYAKU
		  FROM dbo.M_DENSHI_HAIKI_SHURUI_SAIBUNRUI S
				LEFT JOIN dbo.M_HOUKOKUSHO_BUNRUI HB ON HB.HOUKOKUSHO_BUNRUI_CD = S.HOUKOKUSHO_BUNRUI_CD
		WHERE S.EDI_MEMBER_ID = /*data.EDI_MEMBER_ID*/'0000001'
		  /*IF data.HAIKI_SHURUI_CD != null*/AND S.HAIKI_SHURUI_CD LIKE '%' + /*data.HAIKI_SHURUI_CD*/'0001' + '%'/*END*/
		  /*IF data.HAIKI_SHURUI_SAIBUNRUI_CD != null*/AND S.HAIKI_SHURUI_SAIBUNRUI_CD LIKE '%' + /*data.HAIKI_SHURUI_SAIBUNRUI_CD*/'001' + '%'/*END*/
		  /*IF data.HAIKI_SHURUI_NAME != null*/AND S.HAIKI_SHURUI_NAME LIKE '%' +  /*data.HAIKI_SHURUI_NAME*/'NAME' + '%'/*END*/
		  /*IF data.HOUKOKUSHO_BUNRUI_CD != null*/AND S.HOUKOKUSHO_BUNRUI_CD LIKE '%' +  /*data.HOUKOKUSHO_BUNRUI_CD*/'000001' + '%'/*END*/
		  /*IF data.CREATE_USER != null*/AND S.CREATE_USER LIKE '%' +  /*data.CREATE_USER*/ + '%'/*END*/
		  /*IF data.SEARCH_CREATE_DATE != null*/AND CONVERT(nvarchar, S.CREATE_DATE, 120) LIKE '%' +  /*data.SEARCH_CREATE_DATE*/ + '%'/*END*/
		  /*IF data.SEARCH_UPDATE_DATE != null*/AND CONVERT(nvarchar, S.UPDATE_DATE, 120) LIKE '%' +  /*data.SEARCH_UPDATE_DATE*/ + '%'/*END*/
		  /*IF data.UPDATE_USER != null*/AND S.UPDATE_USER LIKE '%' +  /*data.UPDATE_USER*/ + '%'/*END*/
		  /*IF !deletechuFlg*/AND S.DELETE_FLG = /*deletechuFlg*/0/*END*/
	)

	UNION ALL

	(

		SELECT 
			CONVERT(VARCHAR, '') AS EDI_MEMBER_ID,
			H.HAIKI_SHURUI_CD,
			CONVERT(VARCHAR, '000') AS HAIKI_SHURUI_SAIBUNRUI_CD,
			H.HAIKI_SHURUI_NAME,
			H.HAIKI_BUNRUI,
			H.HAIKI_KBN,
			H.KONGOU_KBN,
			H.HOUKOKUSHO_BUNRUI_CD,
			H.CREATE_USER,
			H.CREATE_DATE,
			H.CREATE_PC,
			H.UPDATE_USER,
			H.UPDATE_DATE,
			H.UPDATE_PC,
			H.DELETE_FLG,
			H.TIME_STAMP,
			ISNULL(HB.HOUKOKUSHO_BUNRUI_NAME_RYAKU,N'') AS HOUKOKUSHO_BUNRUI_NAME_RYAKU
		FROM dbo.M_DENSHI_HAIKI_SHURUI H
				LEFT JOIN dbo.M_HOUKOKUSHO_BUNRUI HB ON HB.HOUKOKUSHO_BUNRUI_CD = H.HOUKOKUSHO_BUNRUI_CD
		/*BEGIN*/WHERE
		  /*IF data.HAIKI_SHURUI_CD != null*/H.HAIKI_SHURUI_CD LIKE '%' + /*data.HAIKI_SHURUI_CD*/'0001' + '%'/*END*/
		  /*IF data.HAIKI_SHURUI_SAIBUNRUI_CD != null*/AND '000' LIKE '%' + /*data.HAIKI_SHURUI_SAIBUNRUI_CD*/'001' + '%'/*END*/
		  /*IF data.HAIKI_SHURUI_NAME != null*/AND H.HAIKI_SHURUI_NAME LIKE '%' +  /*data.HAIKI_SHURUI_NAME*/'NAME' + '%'/*END*/
		  /*IF data.HOUKOKUSHO_BUNRUI_CD != null*/AND H.HOUKOKUSHO_BUNRUI_CD LIKE '%' +  /*data.HOUKOKUSHO_BUNRUI_CD*/'000001' + '%'/*END*/
		  /*IF data.CREATE_USER != null*/AND H.CREATE_USER LIKE '%' +  /*data.CREATE_USER*/ + '%'/*END*/
		  /*IF data.SEARCH_CREATE_DATE != null*/AND CONVERT(nvarchar, H.CREATE_DATE, 120) LIKE '%' +  /*data.SEARCH_CREATE_DATE*/ + '%'/*END*/
		  /*IF data.SEARCH_UPDATE_DATE != null*/AND CONVERT(nvarchar, H.UPDATE_DATE, 120) LIKE '%' +  /*data.SEARCH_UPDATE_DATE*/ + '%'/*END*/
		  /*IF data.UPDATE_USER != null*/AND H.UPDATE_USER LIKE '%' +  /*data.UPDATE_USER*/ + '%'/*END*/
		  /*IF !deletechuFlg*/AND H.DELETE_FLG = /*deletechuFlg*/0/*END*/
		/*END*/

	)) T
ORDER BY T.HAIKI_SHURUI_CD, T.HAIKI_SHURUI_SAIBUNRUI_CD
