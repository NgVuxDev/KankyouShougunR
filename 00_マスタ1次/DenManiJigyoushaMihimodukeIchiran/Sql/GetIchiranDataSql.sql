SELECT
	*,
	M_DENSHI_JIGYOUSHA.JIGYOUSHA_ADDRESS1 + M_DENSHI_JIGYOUSHA.JIGYOUSHA_ADDRESS2 + M_DENSHI_JIGYOUSHA.JIGYOUSHA_ADDRESS3 + M_DENSHI_JIGYOUSHA.JIGYOUSHA_ADDRESS4 AS JIGYOUSHA_ADDRESS,
	CASE
		WHEN HST_KBN = 1 THEN 1
		WHEN UPN_KBN = 1 THEN 2
		WHEN SBN_KBN = 1 THEN 3
	END JIGYOUSHA_KBN
  FROM M_DENSHI_JIGYOUSHA
 WHERE GYOUSHA_CD IS NULL OR GYOUSHA_CD = ''