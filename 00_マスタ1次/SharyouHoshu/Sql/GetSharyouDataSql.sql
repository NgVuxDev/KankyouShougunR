SELECT 
    SHARYOU.*
    ,ISNULL(GYO.GYOUSHA_NAME_RYAKU,N'') AS GYOUSHA_NAME_RYAKU
    ,ISNULL(SHA.SHASHU_NAME_RYAKU,N'') AS SHASHU_NAME_RYAKU
    ,ISNULL(SHAIN.SHAIN_NAME_RYAKU,N'') AS SHAIN_NAME_RYAKU
FROM 
    dbo.M_SHARYOU SHARYOU
    LEFT JOIN dbo.M_GYOUSHA GYO ON GYO.GYOUSHA_CD = SHARYOU.GYOUSHA_CD
    LEFT JOIN dbo.M_SHASHU SHA ON SHA.SHASHU_CD = SHARYOU.SHASYU_CD
    LEFT JOIN dbo.M_SHAIN SHAIN ON SHAIN.SHAIN_CD = SHARYOU.SHAIN_CD
WHERE /*IF data.GYOUSHA_CD != null*/SHARYOU.GYOUSHA_CD = /*data.GYOUSHA_CD*/''
-- ELSE SHARYOU.GYOUSHA_CD = ''
/*END*/
  /*IF data.SHARYOU_CD != null*/AND SHARYOU.SHARYOU_CD LIKE '%' + /*data.SHARYOU_CD*/ + '%'/*END*/
  /*IF data.SHARYOU_NAME != null*/AND SHARYOU.SHARYOU_NAME LIKE '%' + /*data.SHARYOU_NAME*/ + '%'/*END*/
  /*IF data.SHARYOU_NAME_RYAKU != null*/AND SHARYOU.SHARYOU_NAME_RYAKU LIKE '%' + /*data.SHARYOU_NAME_RYAKU*/ + '%'/*END*/
  /*IF data.SHASYU_CD != null*/AND SHARYOU.SHASYU_CD LIKE '%' + /*data.SHASYU_CD*/ + '%'/*END*/
  /*IF data.SHAIN_CD != null*/AND SHARYOU.SHAIN_CD LIKE '%' + /*data.SHAIN_CD*/ + '%'/*END*/
  /*IF !data.SAIDAI_SEKISAI.IsNull*/AND SHARYOU.SAIDAI_SEKISAI=/*data.SAIDAI_SEKISAI.Value*/0/*END*/ 
  /*IF !data.KUUSHA_JYURYO.IsNull*/AND SHARYOU.KUUSHA_JYURYO= /*data.KUUSHA_JYURYO.Value*/0/*END*/ 
  /*IF data.SHARYOU_BIKOU != null*/AND SHARYOU.SHARYOU_BIKOU LIKE '%' + /*data.SHARYOU_BIKOU*/ + '%'/*END*/
  /*IF !data.SAIDAI_WAKU_SUU.IsNull*/AND SHARYOU.SAIDAI_WAKU_SUU= /*data.SAIDAI_WAKU_SUU.Value*/0/*END*/ 
  /*IF data.CREATE_USER != null*/AND SHARYOU.CREATE_USER LIKE '%' +  /*data.CREATE_USER*/ + '%'/*END*/
  /*IF data.SEARCH_CREATE_DATE != null*/AND CONVERT(nvarchar, SHARYOU.CREATE_DATE, 120) LIKE '%' +  /*data.SEARCH_CREATE_DATE*/ + '%'/*END*/
  /*IF data.UPDATE_USER != null*/AND SHARYOU.UPDATE_USER LIKE '%' +  /*data.UPDATE_USER*/ + '%'/*END*/
  /*IF data.SEARCH_UPDATE_DATE != null*/AND CONVERT(nvarchar, SHARYOU.UPDATE_DATE, 120) LIKE '%' +  /*data.SEARCH_UPDATE_DATE*/ + '%'/*END*/
ORDER BY SHARYOU.SHARYOU_CD
