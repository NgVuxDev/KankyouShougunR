SELECT 
    SHOU.SYS_ID,
	(SHOU.SHOUHIZEI_RATE * 100) SHOUHIZEI_RATE,
	SHOU.TEKIYOU_BEGIN,
	SHOU.TEKIYOU_END,
	SHOU.CREATE_USER,
	SHOU.CREATE_DATE,
	SHOU.CREATE_PC,
	SHOU.UPDATE_USER,
	SHOU.UPDATE_DATE,
	SHOU.UPDATE_PC,
	SHOU.DELETE_FLG,
	SHOU.TIME_STAMP
FROM 
    dbo.M_SHOUHIZEI SHOU
/*BEGIN*/WHERE
 /*IF !data.SYS_ID.IsNull */SHOU.SYS_ID = /*data.SYS_ID*/0/*END*/
 /*IF !data.SHOUHIZEI_RATE.IsNull */AND SHOU.SHOUHIZEI_RATE = /*data.SHOUHIZEI_RATE*/0/*END*/
 /*IF data.SEARCH_TEKIYOU_BEGIN != null*/AND CONVERT(nvarchar, SHOU.TEKIYOU_BEGIN, 120) LIKE '%' +  /*data.SEARCH_TEKIYOU_BEGIN*/ + '%'/*END*/
 /*IF data.SEARCH_TEKIYOU_END != null*/AND CONVERT(nvarchar, SHOU.TEKIYOU_END, 120) LIKE '%' +  /*data.SEARCH_TEKIYOU_END*/ + '%'/*END*/
 /*IF data.CREATE_USER != null*/AND SHOU.CREATE_USER LIKE '%' +  /*data.CREATE_USER*/ + '%'/*END*/
 /*IF data.SEARCH_CREATE_DATE != null*/AND CONVERT(nvarchar, SHOU.CREATE_DATE, 120) LIKE '%' +  /*data.SEARCH_CREATE_DATE*/ + '%'/*END*/
 /*IF data.UPDATE_USER != null*/AND SHOU.UPDATE_USER LIKE '%' +  /*data.UPDATE_USER*/ + '%'/*END*/
 /*IF data.SEARCH_UPDATE_DATE != null*/AND CONVERT(nvarchar, SHOU.UPDATE_DATE, 120) LIKE '%' +  /*data.SEARCH_UPDATE_DATE*/ + '%'/*END*/
/*END*/
ORDER BY SHOU.SYS_ID
