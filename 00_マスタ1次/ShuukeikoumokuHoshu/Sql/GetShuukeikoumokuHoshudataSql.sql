SELECT
    SHUUKEI.*
    ,CAST(0 AS bit) AS DELETE_FLG
FROM
    dbo.M_SHUUKEI_KOUMOKU SHUUKEI
/*BEGIN*/WHERE
    /*IF data.SHUUKEI_KOUMOKU_CD != null*/ SHUUKEI.SHUUKEI_KOUMOKU_CD LIKE '%' + /*data.SHUUKEI_KOUMOKU_CD*/ + '%'/*END*/
    /*IF data.SHUUKEI_KOUMOKU_NAME != null*/AND SHUUKEI.SHUUKEI_KOUMOKU_NAME LIKE '%' +  /*data.SHUUKEI_KOUMOKU_NAME*/ + '%'/*END*/
    /*IF data.UPDATE_USER != null*/AND SHUUKEI.UPDATE_USER LIKE '%' +  /*data.UPDATE_USER*/ + '%'/*END*/
    /*IF data.SEARCH_UPDATE_DATE != null*/AND CONVERT(nvarchar, SHUUKEI.UPDATE_DATE, 120) LIKE '%' +  /*data.SEARCH_UPDATE_DATE*/ + '%'/*END*/
    /*IF data.CREATE_USER != null*/AND SHUUKEI.CREATE_USER LIKE '%' +  /*data.CREATE_USER*/ + '%'/*END*/
    /*IF data.SEARCH_CREATE_DATE != null*/AND CONVERT(nvarchar, SHUUKEI.CREATE_DATE, 120) LIKE '%' +  /*data.SEARCH_CREATE_DATE*/ + '%'/*END*/
/*END*/
ORDER BY SHUUKEI.SHUUKEI_KOUMOKU_CD
