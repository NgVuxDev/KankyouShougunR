SELECT
NYD.SYSTEM_ID AS SYSTEM_ID,
NYD.SEQ AS SEQ,
NYD.DETAIL_SYSTEM_ID AS DETAIL_SYSTEM_ID,
NYD.ROW_NUMBER AS ROW_NO,
NYE.NYUUKIN_NUMBER AS DENPYOU_NUMBER,
NYE.DENPYOU_DATE AS DENPYOU_DATE,
NYE.KYOTEN_CD AS KYOTEN_CD
FROM
T_NYUUKIN_ENTRY NYE RIGHT OUTER JOIN T_NYUUKIN_DETAIL NYD
ON NYE.SYSTEM_ID = NYD.SYSTEM_ID AND NYE.SEQ = NYD.SEQ
/*BEGIN*/WHERE
/*IF !deletechuFlg*/NYE.DELETE_FLG = 0/*END*/
/*IF data.KYOTEN_CD != 99*/
 AND NYE.KYOTEN_CD = /*data.KYOTEN_CD*//*END*/
/*IF data.SEIKYUSHIMEBI_FROM != null && data.SEIKYUSHIMEBI_FROM != ""*/
 AND CONVERT(DATETIME, NYE.DENPYOU_DATE,111) >= CONVERT(DATETIME, /*data.SEIKYUSHIMEBI_FROM*/null,111)
 AND CONVERT(DATETIME, NYE.DENPYOU_DATE,111) <= CONVERT(DATETIME, /*data.SEIKYUSHIMEBI_TO*/null,111)
  -- ELSE  AND CONVERT(DATETIME, NYE.DENPYOU_DATE,111) <= CONVERT(DATETIME, /*data.SEIKYUSHIMEBI_TO*/null,111)/*END*/
 AND NYE.TORIHIKISAKI_CD = /*data.SEIKYU_CD*/
/*END*/