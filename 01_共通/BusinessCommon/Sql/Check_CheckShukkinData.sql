SHD.SYSTEM_ID AS SYSTEM_ID,
SHD.SEQ AS SEQ,
SHD.DETAIL_SYSTEM_ID AS DETAIL_SYSTEM_ID,
SHD.ROW_NUMBER AS ROW_NO,
SHE.SHUKKIN_NUMBER AS DENPYOU_NUMBER,
SHE.DENPYOU_DATE AS DENPYOU_DATE,
SHE.KYOTEN_CD AS KYOTEN_CD
