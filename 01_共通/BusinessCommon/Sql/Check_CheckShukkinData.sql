T_SHUKKIN_ENTRY SHE RIGHT OUTER JOIN T_SHUKKIN_DETAIL SHD
ON SHE.SYSTEM_ID = SHD.SYSTEM_ID AND SHE.SEQ = SHD.SEQ
/*BEGIN*/WHERE
/*IF !deletechuFlg*/SHE.DELETE_FLG = 0/*END*/
