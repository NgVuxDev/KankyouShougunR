	'' AS TORIHIKISAKI_CD
	,TMLZ.YEAR
	,TMLZ.MONTH
	,'在庫' AS KUBUN
FROM
	T_MONTHLY_LOCK_ZAIKO TMLZ
WHERE
	TMLZ.DELETE_FLG = 0
	/*IF !data.YEAR.IsNull*/ AND TMLZ.YEAR >= /*data.YEAR*/2015 /*END*/