    WHERE M_GYOUSHA.GYOUSHA_CD = M_GENBA.GYOUSHA_CD 
     /*IF data.TSUMIKAEHOKAN_KBN != null && data.TSUMIKAEHOKAN_KBN != ''*/
     AND  M_GENBA.TSUMIKAEHOKAN_KBN = /*data.TSUMIKAEHOKAN_KBN*/
	 /*END*/
     /*IF data.SAISHUU_SHOBUNJOU_KBN != null && data.SAISHUU_SHOBUNJOU_KBN != ''*/
