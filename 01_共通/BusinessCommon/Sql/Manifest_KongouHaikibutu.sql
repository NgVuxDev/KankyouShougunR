SELECT MKH.HAIKI_SHURUI_CD
     , MHS.HAIKI_SHURUI_NAME_RYAKU
     , MKH.HAIKI_HIRITSU
  FROM M_KONGOU_HAIKIBUTSU MKH
  LEFT OUTER JOIN M_HAIKI_SHURUI MHS 
    ON MHS.HAIKI_KBN_CD = MKH.HAIKI_KBN_CD 
   AND MHS.HAIKI_SHURUI_CD = MKH.HAIKI_SHURUI_CD
   AND MHS.DELETE_FLG = 'false'
 WHERE MKH.DELETE_FLG = 'false'
   AND MKH.HAIKI_KBN_CD = /*data.HAIKI_KBN_CD*/
   AND MKH.KONGOU_SHURUI_CD = /*data.KONGOU_SHURUI_CD*/
