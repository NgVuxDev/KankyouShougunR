	MT.TORIHIKISAKI_CD
/*IF joken == 0 || joken == 1 */
	,MTS.SHIMEBI1
	,MTS.SHIMEBI2
	,MTS.SHIMEBI3
/*END*/
FROM
	M_TORIHIKISAKI AS MT
	/*IF joken == 0*/LEFT JOIN M_TORIHIKISAKI_SEIKYUU AS MTS ON MT.TORIHIKISAKI_CD = MTS.TORIHIKISAKI_CD/*END*/
	/*IF joken == 1*/LEFT JOIN M_TORIHIKISAKI_SHIHARAI AS MTS ON MT.TORIHIKISAKI_CD = MTS.TORIHIKISAKI_CD/*END*/

WHERE
/*IF torihikisakiCd != ""*/
	MT.TORIHIKISAKI_CD = /*torihikisakiCd*/
	/*IF joken == 0 || joken == 1*/AND MTS.TORIHIKI_KBN_CD = 2/*END*/
	/*IF joken == 2*/AND MT.MANI_HENSOUSAKI_KBN = 1/*END*/
/*END*/