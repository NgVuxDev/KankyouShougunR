	T1.NYUUKINSAKI_KBN AS TORIHIKISAKI_NYUUKINSAKI_KBN,
	T1.TORIHIKISAKI_KYOTEN_CD AS TORIHIKISAKI_KYOTEN_CD,
	T2.KYOTEN_NAME_RYAKU AS KYOTEN_NAME_RYAKU,
	T1.TORIHIKISAKI_CD AS TORIHIKISAKI_CD,
	T1.TORIHIKISAKI_FURIGANA AS TORIHIKISAKI_FURIGANA,
	T1.TORIHIKISAKI_NAME1 AS TORIHIKISAKI_NAME1,
	T1.TORIHIKISAKI_KEISHOU1 AS TORIHIKISAKI_KEISHOU1,
	T1.TORIHIKISAKI_NAME2 AS TORIHIKISAKI_NAME2,
	T1.TORIHIKISAKI_KEISHOU2 AS TORIHIKISAKI_KEISHOU2,
	T1.TORIHIKISAKI_NAME_RYAKU AS TORIHIKISAKI_NAME_RYAKU,
	T1.TORIHIKISAKI_TEL AS TORIHIKISAKI_TEL,
	T1.TORIHIKISAKI_FAX AS TORIHIKISAKI_FAX,
	T1.EIGYOU_TANTOU_BUSHO_CD AS EIGYOU_TANTOU_BUSHO_CD,
	T3.BUSHO_NAME_RYAKU AS EIGYOU_TANTOU_BUSHO_NAME,
	T1.EIGYOU_TANTOU_CD AS EIGYOU_TANTOU_CD,
	T4.SHAIN_NAME_RYAKU AS EIGYOU_TANTOU_NAME,
