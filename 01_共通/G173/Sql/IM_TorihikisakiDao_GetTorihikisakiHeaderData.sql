	T1.TEKIYOU_END AS TORIHIKISAKI_TEKIYOU_END,
	T1.CHUUSHI_RIYUU1 AS CHUUSHI_RIYUU1,
	T1.CHUUSHI_RIYUU2 AS CHUUSHI_RIYUU2,
	T1.CREATE_USER AS CREATE_USER,
	T1.CREATE_DATE AS CREATE_DATE,
	T1.UPDATE_USER AS UPDATE_USER,
	T1.UPDATE_DATE AS UPDATE_DATE

FROM dbo.M_TORIHIKISAKI AS T1 
LEFT JOIN dbo.M_KYOTEN AS T2 ON T1.TORIHIKISAKI_KYOTEN_CD = T2.KYOTEN_CD 
LEFT JOIN dbo.M_BUSHO AS T3 ON T1.EIGYOU_TANTOU_BUSHO_CD = T3.BUSHO_CD 
LEFT JOIN dbo.M_SHAIN AS T4 ON T1.EIGYOU_TANTOU_CD = T4.SHAIN_CD 

WHERE 
1=1
AND T1.TORIHIKISAKI_CD = /*torihikisakiCd*/