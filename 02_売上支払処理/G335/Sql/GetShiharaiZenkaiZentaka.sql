SELECT
	 ISNULL(SEIDEN.KONKAI_SEISAN_GAKU, 0) AS SEISAN_ZANDAKA,
	 SEIDEN.SEISAN_NUMBER,
	 SEIDEN.SEISAN_DATE AS EXEC_DATE
FROM
	T_SEISAN_DENPYOU AS SEIDEN
WHERE (SEIDEN.DELETE_FLG = 0) 
/*IF torihikisakiCD != null*/AND SEIDEN.TORIHIKISAKI_CD = /*torihikisakiCD*//*END*/
/*IF startDay != null*/AND CONVERT(DATETIME, CONVERT(nvarchar, SEIDEN.SEISAN_DATE, 111), 120) = (
	SELECT
		MAX(T_SEISAN_DENPYOU.SEISAN_DATE)
	FROM
		T_SEISAN_DENPYOU
	WHERE
		T_SEISAN_DENPYOU.DELETE_FLG = 0 AND
		T_SEISAN_DENPYOU.SEISAN_DATE <= /*startDay*/
		/*IF torihikisakiCD != null*/AND T_SEISAN_DENPYOU.TORIHIKISAKI_CD = /*torihikisakiCD*//*END*/)/*END*/
ORDER BY SEIDEN.SEISAN_NUMBER DESC

