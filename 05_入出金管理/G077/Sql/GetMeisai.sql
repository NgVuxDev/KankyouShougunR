SELECT
DETAIL.DETAIL_SYSTEM_ID,
DETAIL.ROW_NUMBER,
RIGHT('00' + convert(varchar,DETAIL.NYUUSHUKKIN_KBN_CD),2) as NYUUSHUKKIN_KBN_CD,
DETAIL.KINGAKU,
DETAIL.MEISAI_BIKOU,
KBN.NYUUSHUKKIN_KBN_NAME_RYAKU
FROM
T_NYUUKIN_ENTRY NYUUKIN, 
T_NYUUKIN_DETAIL DETAIL 
LEFT OUTER JOIN M_NYUUSHUKKIN_KBN KBN ON KBN.NYUUSHUKKIN_KBN_CD = DETAIL.NYUUSHUKKIN_KBN_CD 
/*BEGIN*/
WHERE 
/*IF !deletechuFlg*/
	NYUUKIN.DELETE_FLG = 0 
	AND NYUUKIN.SYSTEM_ID = DETAIL.SYSTEM_ID
	AND NYUUKIN.SEQ = DETAIL.SEQ 
/*END*/
/*IF data.Nyukin_number != null && data.Nyukin_number != ''*/
	AND	NYUUKIN.NYUUKIN_NUMBER = /*data.Nyukin_number*/
/*END*/
ORDER BY DETAIL.ROW_NUMBER ASC
/*END*/
