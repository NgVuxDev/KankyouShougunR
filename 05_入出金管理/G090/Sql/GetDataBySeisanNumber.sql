SELECT
    *
FROM
    T_SEISAN_DETAIL
    LEFT JOIN
        T_SHUKKIN_KESHIKOMI
    ON  T_SEISAN_DETAIL.DENPYOU_NUMBER = T_SHUKKIN_KESHIKOMI.SHUKKIN_NUMBER
    AND T_SHUKKIN_KESHIKOMI.DELETE_FLG = 0
WHERE
    T_SEISAN_DETAIL.DENPYOU_SHURUI_CD = 20
AND T_SEISAN_DETAIL.DELETE_FLG = 0
AND T_SHUKKIN_KESHIKOMI.SEISAN_NUMBER IS NULL
AND T_SEISAN_DETAIL.SEISAN_NUMBER = /*seisanNumber*/