SELECT 
	TSD.SEISAN_NUMBER
	,ISNULL(TSD_INXS.UPLOAD_STATUS,1) AS UPLOAD_STATUS
	,ISNULL(TSD_INXS.DOWNLOAD_STATUS,1) AS DOWNLOAD_STATUS
	,TSD.TIME_STAMP
FROM T_SEISAN_DENPYOU TSD
	LEFT JOIN T_SEISAN_DENPYOU_INXS TSD_INXS
            ON TSD_INXS.SEISAN_NUMBER = TSD.SEISAN_NUMBER
WHERE 
	TSD.SEISAN_NUMBER IN /*seisanNumber*/()
	AND TSD.DELETE_FLG = 0