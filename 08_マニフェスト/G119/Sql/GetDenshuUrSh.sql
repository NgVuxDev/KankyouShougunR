SELECT
TUSE.DENPYOU_DATE as KOUFU_DATE,
TUSE.TORIHIKISAKI_CD as TORIHIKISAKI_CD,
TUSE.TORIHIKISAKI_NAME as TORIHIKISAKI_NAME,
case when TUSE.NIZUMI_GYOUSHA_CD is not null and TUSE.NIZUMI_GYOUSHA_CD != ''
then TUSE.NIZUMI_GYOUSHA_CD
else TUSE.GYOUSHA_CD
end as HAISYUTU_GYOUSHA_CD,
case when TUSE.NIZUMI_GYOUSHA_NAME is not null and TUSE.NIZUMI_GYOUSHA_NAME != ''
then TUSE.NIZUMI_GYOUSHA_NAME
else TUSE.GYOUSHA_NAME
end as GYOUSHA_NAME,
case when TUSE.NIZUMI_GENBA_CD is not null and TUSE.NIZUMI_GENBA_CD != ''
then TUSE.NIZUMI_GENBA_CD
else TUSE.GENBA_CD
end as HAISYUTU_GENBA_CD,
case when TUSE.NIZUMI_GENBA_NAME is not null and TUSE.NIZUMI_GENBA_NAME != ''
then TUSE.NIZUMI_GENBA_NAME
else TUSE.GENBA_NAME
end as GENBA_NAME,
TUSE.UNPAN_GYOUSHA_CD as UNPAN_GYOUSHA_CD,
TUSE.UNPAN_GYOUSHA_NAME as UNPAN_GYOUSHA_NAME,
TUSE.NIOROSHI_GYOUSHA_CD as SYOBUN_GYOUSHA_CD,
TUSE.NIOROSHI_GYOUSHA_NAME as SYOBUN_GYOUSHA_NAME,
TUSE.NIOROSHI_GENBA_CD as UNPAN_GENBA_CD,
TUSE.NIOROSHI_GENBA_NAME as NIOROSHI_GENBA_NAME,
TUSE.SHASHU_CD as SHASHU_CD,
TUSE.SHASHU_NAME as SHASHU_NAME,
TUSE.SHARYOU_CD as SHARYOU_CD,
TUSE.SHARYOU_NAME as SHARYOU_NAME,
TUSE.UNTENSHA_CD as UNTENSHA_CD,
TUSE.UNTENSHA_NAME as UNTENSHA_NAME,
TUSE.NIOROSHI_GYOUSHA_CD as NIOROSHI_GYOUSHA_CD,
TUSE.NIOROSHI_GYOUSHA_NAME as NIOROSHI_GYOUSHA_NAME,
TUSE.DENPYOU_DATE as UNPAN_SYURYOU_DATA,
MHS.HAIKI_SHURUI_CD as HAIKI_SHURUI_CD,
MHS.HAIKI_SHURUI_NAME_RYAKU as HAIKI_SHURUI_NAME_RYAKU,
TUSE.SYSTEM_ID as SYSTEM_ID,
TUSD.DETAIL_SYSTEM_ID as DETAIL_SYSTEM_ID,
TUSE.MANIFEST_SHURUI_CD as MANIFEST_SHURUI_CD
FROM 
(SELECT TOP 1 TUSE.* FROM T_UR_SH_ENTRY TUSE
left join T_UR_SH_DETAIL TUSD ON (TUSE.SYSTEM_ID = TUSD.SYSTEM_ID AND TUSE.SEQ = TUSD.SEQ)
WHERE TUSE.UR_SH_NUMBER =  /*data.RENKEI_ID*/0
AND TUSE.DELETE_FLG = 0
/*IF data.RENKEI_MEISAI_ID != NULL && data.RENKEI_MEISAI_ID != ''*/
AND TUSD.ROW_NO = /*data.RENKEI_MEISAI_ID*/0
/*END*/
) TUSE
left join T_UR_SH_DETAIL TUSD ON (TUSE.SYSTEM_ID = TUSD.SYSTEM_ID AND TUSE.SEQ = TUSD.SEQ)
left join M_HINMEI MH ON (MH.HINMEI_CD = TUSD.HINMEI_CD)
left join M_HAIKI_SHURUI MHS ON (MHS.HAIKI_SHURUI_CD = MH.SP_CHOKKOU_HAIKI_SHURUI_CD
                              AND MHS.HAIKI_KBN_CD = 1)
WHERE TUSE.UR_SH_NUMBER =  /*data.RENKEI_ID*/0
AND TUSE.DELETE_FLG = 0
/*IF data.RENKEI_MEISAI_ID != NULL && data.RENKEI_MEISAI_ID != ''*/
AND TUSD.ROW_NO = /*data.RENKEI_MEISAI_ID*/0
/*END*/
