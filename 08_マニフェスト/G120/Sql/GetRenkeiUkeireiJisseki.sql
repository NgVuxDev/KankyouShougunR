SELECT
TUE.DENPYOU_DATE AS KOHU_DATE,
TUE.TORIHIKISAKI_CD,
TUE.TORIHIKISAKI_NAME,
TUE.GYOUSHA_CD,
TUE.GYOUSHA_NAME,
TUE.GENBA_CD,
TUE.GENBA_NAME,
TUE.UNPAN_GYOUSHA_CD,
TUE.UNPAN_GYOUSHA_NAME,
TUE.SHASHU_CD,
TUE.SHASHU_NAME,
TUE.SHARYOU_CD,
TUE.SHARYOU_NAME,
TUE.UNTENSHA_CD,
TUE.UNTENSHA_NAME,
TUE.NIOROSHI_GENBA_CD,
TUE.NIOROSHI_GENBA_NAME,
TUE.NIOROSHI_GYOUSHA_CD,
TUE.NIOROSHI_GYOUSHA_NAME,
TUE.DENPYOU_DATE AS UNBAN_DATE,
MHS.HAIKI_SHURUI_CD,
MHS.HAIKI_SHURUI_NAME_RYAKU,
GB.TSUMIKAEHOKAN_KBN,
GB.SHOBUN_NIOROSHI_GENBA_KBN,
TUE.SYSTEM_ID,
UJD.DETAIL_SYSTEM_ID as DETAIL_SYSTEM_ID,
TUE.MANIFEST_SHURUI_CD,
ISNULL(TUE.NET_TOTAL,0) AS NET_TOTAL,
ISNULL(UJD.SUURYOU_WARIAI,0) AS SUURYOU_WARIAI,
M_UNIT.UNIT_NAME
FROM(SELECT TOP 1 TUE.*, UJE.SEQ AS UDE_SEQ, UJD.DETAIL_SEQ AS UJD_DETAIL_SEQ, 3 AS UNIT_CD  FROM T_UKEIRE_ENTRY TUE
left join T_UKEIRE_JISSEKI_ENTRY UJE ON (TUE.SYSTEM_ID = UJE.DENPYOU_SYSTEM_ID AND UJE.DENPYOU_SHURUI = 2 AND UJE.DELETE_FLG = 0) 
left join T_UKEIRE_JISSEKI_DETAIL UJD ON (UJE.DENPYOU_SHURUI = UJD.DENPYOU_SHURUI AND UJE.DENPYOU_SYSTEM_ID = UJD.DENPYOU_SYSTEM_ID AND UJE.SEQ = UJD.SEQ)
WHERE TUE.UKEIRE_NUMBER =  /*data.RENKEI_NO*/0
AND TUE.DELETE_FLG = 0 
/*IF data.RENKEI_GYO_NO != NULL && data.RENKEI_GYO_NO !=''*/
 AND UJD.DETAIL_SEQ = /*data.RENKEI_GYO_NO*/0
/*END*/	) TUE
left join T_UKEIRE_JISSEKI_DETAIL UJD ON (TUE.SYSTEM_ID = UJD.DENPYOU_SYSTEM_ID AND TUE.UDE_SEQ = UJD.SEQ AND UJD.DENPYOU_SHURUI = 2)
left join M_HINMEI MH ON (MH.HINMEI_CD = UJD.HINMEI_CD)
left join M_HAIKI_SHURUI MHS ON (MHS.HAIKI_SHURUI_CD = MH.SP_TSUMIKAE_HAIKI_SHURUI_CD
							  AND MHS.HAIKI_KBN_CD = 3)
left join M_GENBA GB ON (TUE.NIOROSHI_GYOUSHA_CD = GB.GYOUSHA_CD
					 AND TUE.NIOROSHI_GENBA_CD = GB.GENBA_CD)
left join M_UNIT ON TUE.UNIT_CD = M_UNIT.UNIT_CD
WHERE TUE.UKEIRE_NUMBER=  /*data.RENKEI_NO*/0
AND TUE.DELETE_FLG = 0 
/*IF data.RENKEI_GYO_NO != NULL && data.RENKEI_GYO_NO !=''*/
 AND UJD.DETAIL_SEQ = /*data.RENKEI_GYO_NO*/0
/*END*/	
/*IF (data.RENKEI_GYO_NO == NULL || data.RENKEI_GYO_NO == '') && data.CHK_MODE_KBN == 1 */
AND UJD.SUURYOU_WARIAI IS NOT NULL
/*END*/