SELECT 
TUSSE.SAGYOU_DATE,
TUSSE.TORIHIKISAKI_CD,
TUSSE.TORIHIKISAKI_NAME,
TUSSE.GYOUSHA_CD,
TUSSE.GYOUSHA_NAME,
TUSSE.GENBA_CD,
TUSSE.GENBA_NAME,
TUSSE.UNPAN_GYOUSHA_CD,
TUSSE.UNPAN_GYOUSHA_NAME,
TUSSE.SHASHU_CD,
TUSSE.SHASHU_NAME,
TUSSE.SHARYOU_CD,
TUSSE.SHARYOU_NAME,
TUSSE.NIOROSHI_GENBA_CD,
TUSSE.NIOROSHI_GENBA_NAME,
TUSSE.NIOROSHI_GYOUSHA_CD,
TUSSE.NIOROSHI_GYOUSHA_NAME,
TUSSE.UNTENSHA_CD,
TUSSE.UNTENSHA_NAME,
TUSSE.SAGYOU_DATE as NIOROSHI_DATE,
MHS.HAIKI_SHURUI_CD,
MHS.HAIKI_SHURUI_NAME_RYAKU,
GB.TSUMIKAEHOKAN_KBN,
GB.SHOBUN_NIOROSHI_GENBA_KBN,
TUSSE.SYSTEM_ID,
TUSSD.DETAIL_SYSTEM_ID,
TUSSE.MANIFEST_SHURUI_CD
FROM (SELECT TOP 1 TUSSE.* FROM  T_UKETSUKE_SS_ENTRY TUSSE 
left join T_UKETSUKE_SS_DETAIL TUSSD ON (TUSSE.SYSTEM_ID = TUSSD.SYSTEM_ID AND TUSSE.SEQ = TUSSD.SEQ)
WHERE TUSSE.UKETSUKE_NUMBER = /*data.RENKEI_NO*/0							
AND TUSSE.DELETE_FLG = 0	
/*IF data.RENKEI_GYO_NO != NULL && data.RENKEI_GYO_NO !=''*/
AND TUSSD.ROW_NO         = /*data.RENKEI_GYO_NO*/0
/*END*/	) TUSSE
left join T_UKETSUKE_SS_DETAIL TUSSD ON (TUSSE.SYSTEM_ID = TUSSD.SYSTEM_ID AND TUSSE.SEQ = TUSSD.SEQ)
left join M_HINMEI MH ON (MH.HINMEI_CD = TUSSD.HINMEI_CD) 
left join M_HAIKI_SHURUI MHS ON (MHS.HAIKI_SHURUI_CD = MH.SP_TSUMIKAE_HAIKI_SHURUI_CD
							  AND MHS.HAIKI_KBN_CD = 3)
left join M_GENBA GB ON (TUSSE.NIOROSHI_GYOUSHA_CD = GB.GYOUSHA_CD
					 AND TUSSE.NIOROSHI_GENBA_CD = GB.GENBA_CD)

WHERE TUSSE.UKETSUKE_NUMBER = /*data.RENKEI_NO*/0			
AND TUSSE.DELETE_FLG = 0	
/*IF data.RENKEI_GYO_NO != NULL && data.RENKEI_GYO_NO !=''*/
AND TUSSD.ROW_NO         = /*data.RENKEI_GYO_NO*/0
/*END*/	