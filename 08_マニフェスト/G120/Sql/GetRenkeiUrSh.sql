SELECT 
TUSE.DENPYOU_DATE AS KOHU_DATE,
TUSE.TORIHIKISAKI_CD,
TUSE.TORIHIKISAKI_NAME,
case when TUSE.NIZUMI_GYOUSHA_CD is not null and TUSE.NIZUMI_GYOUSHA_CD != ''
then TUSE.NIZUMI_GYOUSHA_CD
else TUSE.GYOUSHA_CD
end as GYOUSHA_CD,
case when TUSE.NIZUMI_GYOUSHA_NAME is not null and TUSE.NIZUMI_GYOUSHA_NAME != ''
then TUSE.NIZUMI_GYOUSHA_NAME
else TUSE.GYOUSHA_NAME
end as GYOUSHA_NAME,
case when TUSE.NIZUMI_GENBA_CD is not null and TUSE.NIZUMI_GENBA_CD != ''
then TUSE.NIZUMI_GENBA_CD
else TUSE.GENBA_CD
end as GENBA_CD,
case when TUSE.NIZUMI_GENBA_NAME is not null and TUSE.NIZUMI_GENBA_NAME != ''
then TUSE.NIZUMI_GENBA_NAME
else TUSE.GENBA_NAME
end as GENBA_NAME,
TUSE.UNPAN_GYOUSHA_CD,
TUSE.UNPAN_GYOUSHA_NAME,
TUSE.SHASHU_CD,
TUSE.SHASHU_NAME,
TUSE.SHARYOU_CD,
TUSE.SHARYOU_NAME,
TUSE.NIOROSHI_GENBA_CD,
TUSE.NIOROSHI_GENBA_NAME,
TUSE.NIOROSHI_GYOUSHA_CD,
TUSE.NIOROSHI_GYOUSHA_NAME,
TUSE.UNTENSHA_CD,
TUSE.UNTENSHA_NAME,
TUSE.DENPYOU_DATE AS UNBAN_DATE,
MHS.HAIKI_SHURUI_CD,
MHS.HAIKI_SHURUI_NAME_RYAKU,
GB.TSUMIKAEHOKAN_KBN,
GB.SHOBUN_NIOROSHI_GENBA_KBN,
TUSE.SYSTEM_ID,
TUSD.DETAIL_SYSTEM_ID,
TUSE.MANIFEST_SHURUI_CD
FROM (SELECT TOP 1 TUSE.* FROM  T_UR_SH_ENTRY TUSE 
left join T_UR_SH_DETAIL TUSD ON (TUSE.SYSTEM_ID = TUSD.SYSTEM_ID AND TUSE.SEQ = TUSD.SEQ)
WHERE TUSE.UR_SH_NUMBER =  /*data.RENKEI_NO*/0										
AND TUSE.DELETE_FLG = 0					
/*IF data.RENKEI_GYO_NO != NULL && data.RENKEI_GYO_NO !=''*/
 AND TUSD.ROW_NO = /*data.RENKEI_GYO_NO*/0
/*END*/	) TUSE
left join T_UR_SH_DETAIL TUSD ON (TUSE.SYSTEM_ID = TUSD.SYSTEM_ID AND TUSE.SEQ = TUSD.SEQ)
left join M_HINMEI MH ON (MH.HINMEI_CD = TUSD.HINMEI_CD)
left join M_HAIKI_SHURUI MHS ON (MHS.HAIKI_SHURUI_CD = MH.SP_TSUMIKAE_HAIKI_SHURUI_CD
							  AND MHS.HAIKI_KBN_CD = 3)
left join M_GENBA GB ON (TUSE.NIOROSHI_GYOUSHA_CD = GB.GYOUSHA_CD
					 AND TUSE.NIOROSHI_GENBA_CD = GB.GENBA_CD)

WHERE TUSE.UR_SH_NUMBER =  /*data.RENKEI_NO*/0					
AND TUSE.DELETE_FLG = 0					
/*IF data.RENKEI_GYO_NO != NULL && data.RENKEI_GYO_NO !=''*/
 AND TUSD.ROW_NO = /*data.RENKEI_GYO_NO*/0
/*END*/	