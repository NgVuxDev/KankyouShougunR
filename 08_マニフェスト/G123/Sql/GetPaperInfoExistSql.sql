----紙マニ一次マニフェスト紐付対象情報取得SQL文
SELECT  TME.SYSTEM_ID AS SYSTEM_ID,
		TME.SEQ AS SEQ,
		TME.KOUFU_DATE AS KOUFU_DATE,				--交付日付
		TME.MANIFEST_ID AS MANIFEST_ID,				--交付番号
		TMD.DETAIL_SYSTEM_ID AS DETAIL_SYSTEM_ID	--マニ明細SYSTEM_ID

FROM T_MANIFEST_ENTRY TME  
INNER JOIN T_MANIFEST_DETAIL TMD ON TME.SYSTEM_ID = TMD.SYSTEM_ID AND TME.SEQ = TMD.SEQ 
WHERE TME.DELETE_FLG = 0 
	  AND TME.FIRST_MANIFEST_KBN = 0
	  AND TMD.LAST_SBN_END_DATE IS NULL
	  AND TMD.DETAIL_SYSTEM_ID  = /*data.SYSTEM_ID*/