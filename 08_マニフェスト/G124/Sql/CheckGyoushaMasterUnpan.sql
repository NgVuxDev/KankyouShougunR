SELECT
    GYOUSHA.GYOUSHA_CD,
    GYOUSHA.GYOUSHA_NAME_RYAKU
FROM
    M_GYOUSHA AS GYOUSHA
WHERE
    GYOUSHA.GYOUSHAKBN_MANI = 1
AND (
        GYOUSHA.HAISHUTSU_NIZUMI_GYOUSHA_KBN = 1
    OR  GYOUSHA.UNPAN_JUTAKUSHA_KAISHA_KBN = 1
    OR  GYOUSHA.SHOBUN_NIOROSHI_GYOUSHA_KBN = 1
    )
/*IF data.DELETE_FLG == false*/
AND GYOUSHA.DELETE_FLG = 0
/*END*/
AND (
        GYOUSHA.UPN_HOUKOKUSHO_TEISHUTSU_CHIIKI_CD IS NULL
    OR  GYOUSHA.UPN_HOUKOKUSHO_TEISHUTSU_CHIIKI_CD = ''
    )