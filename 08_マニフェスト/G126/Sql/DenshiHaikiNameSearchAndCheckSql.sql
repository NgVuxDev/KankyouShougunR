SELECT DISTINCT 
       M_DENSHI_HAIKI_NAME.HAIKI_NAME_CD,
       M_DENSHI_HAIKI_NAME.HAIKI_NAME
  FROM M_DENSHI_HAIKI_NAME (NOLOCK)
LEFT JOIN M_DENSHI_JIGYOUSHA ON M_DENSHI_HAIKI_NAME.EDI_MEMBER_ID = M_DENSHI_JIGYOUSHA.EDI_MEMBER_ID AND HST_KBN = 1
LEFT JOIN M_GYOUSHA ON M_DENSHI_JIGYOUSHA.GYOUSHA_CD = M_GYOUSHA.GYOUSHA_CD
WHERE 
M_GYOUSHA.GYOUSHA_CD = /*data.HST_GYOUSHA_CD*/'0'
ORDER BY 
M_DENSHI_HAIKI_NAME.HAIKI_NAME_CD, M_DENSHI_HAIKI_NAME.HAIKI_NAME