     , ISNULL(TMU2.UPN_HOUHOU_CD,'') 
-- 2014.08.14 #1134 MIYA MOD END
     , TME2.SBN_GYOUSHA_CD 
     , TMU2.UPN_SAKI_GENBA_NAME 
     , TME2.KOUFU_DATE 
     , TME2.HAIKI_KBN_CD 
     , TME2.MANIFEST_ID 
     , TMU2.UPN_ROUTE_NO 
     , TMD2.DETAIL_SYSTEM_ID 
