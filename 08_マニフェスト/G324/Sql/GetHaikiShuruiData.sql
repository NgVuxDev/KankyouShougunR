SELECT	
TME.SYSTEM_ID
,TME.SEQ
,TME.HAIKI_KBN_CD	
,TMD.HAIKI_SHURUI_CD																	
,(SELECT TOP 1 HAIKI_SHURUI_NAME FROM M_HAIKI_SHURUI AS MH  WHERE MH.HAIKI_SHURUI_CD = TMD.HAIKI_SHURUI_CD AND MH.HAIKI_KBN_CD = TME.HAIKI_KBN_CD) HAIKI_SHURUI_NAME	
FROM	T_MANIFEST_ENTRY AS TME 	-- マニフェスト																			
		LEFT JOIN T_MANIFEST_DETAIL AS TMD ON TME.SYSTEM_ID = TMD.SYSTEM_ID AND TME.SEQ = TMD.SEQ
WHERE 	TME.DELETE_FLG = 0 
