UPDATE
    T_MANIFEST_ENTRY
SET
    TOTAL_KANSAN_SUU = (SUM_TABLE.TOTAL_KANSAN_SUU),
    TOTAL_GENNYOU_SUU = (SUM_TABLE.TOTAL_GENNYOU_SUU)
FROM
    T_MANIFEST_ENTRY
    INNER JOIN
        (
            SELECT
                SUM(ISNULL(KANSAN_SUU, 0)) AS TOTAL_KANSAN_SUU,
                SUM(ISNULL(GENNYOU_SUU, 0)) AS TOTAL_GENNYOU_SUU,
                T_MANIFEST_DETAIL.SYSTEM_ID,
                T_MANIFEST_DETAIL.SEQ
            FROM
                T_MANIFEST_DETAIL
                LEFT JOIN
                    T_MANIFEST_ENTRY
                ON  T_MANIFEST_ENTRY.SYSTEM_ID = T_MANIFEST_DETAIL.SYSTEM_ID
                AND T_MANIFEST_ENTRY.SEQ = T_MANIFEST_DETAIL.SEQ
            WHERE
                T_MANIFEST_ENTRY.DELETE_FLG = 0
            GROUP BY
                T_MANIFEST_DETAIL.SYSTEM_ID,
                T_MANIFEST_DETAIL.SEQ
        ) AS SUM_TABLE
    ON  SUM_TABLE.SYSTEM_ID = T_MANIFEST_ENTRY.SYSTEM_ID
    AND SUM_TABLE.SEQ = T_MANIFEST_ENTRY.SEQ
