SELECT R18EX.*
FROM DT_MF_TOC DMT 
INNER JOIN DT_R18 
ON DMT.KANRI_ID = DT_R18.KANRI_ID 
AND DMT.LATEST_SEQ = DT_R18.SEQ 
AND DT_R18.MANIFEST_ID IS NOT NULL 
AND DT_R18.MANIFEST_ID <> '' 
INNER JOIN DT_R18_EX R18EX 
ON R18EX.KANRI_ID = DT_R18.KANRI_ID 
AND R18EX.DELETE_FLG = 0 
INNER JOIN T_MANIFEST_RELATION RE
ON 4 = RE.FIRST_HAIKI_KBN_CD
LEFT JOIN M_GYOUSHA AS HST_GYOUSHA
ON R18EX.HST_GYOUSHA_CD = HST_GYOUSHA.GYOUSHA_CD
WHERE R18EX.DELETE_FLG  = 0
  AND RE.DELETE_FLG  = 0
  AND DT_R18.MANIFEST_ID = /*manifestId*/0
  AND RE.FIRST_SYSTEM_ID = /*detailSystemId*/0
  AND (ISNULL(DT_R18.FIRST_MANIFEST_FLAG, '') = '' OR
       ISNULL(HST_GYOUSHA.JISHA_KBN, 0) = 0)