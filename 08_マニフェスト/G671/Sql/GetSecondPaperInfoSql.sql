----二次マニフェストのデータを取得
SELECT
  TME.SYSTEM_ID AS SYSTEM_ID
  ,TME.SEQ AS SEQ
  ,TMD.DETAIL_SYSTEM_ID
  ,TMD.LAST_SBN_GYOUSHA_CD
  ,TMD.LAST_SBN_GENBA_CD
  ,CASE WHEN COUNT(TMD.LAST_SBN_END_DATE) < COUNT(TMD.DETAIL_SYSTEM_ID) 
    THEN NULL 
    ELSE MAX(TMD.LAST_SBN_END_DATE) END AS LAST_SBN_END_DATE
  ,COUNT(TMD.DETAIL_SYSTEM_ID) AS LINE_COUNT
 FROM T_MANIFEST_ENTRY AS TME WITH(NOLOCK)
	 LEFT JOIN T_MANIFEST_DETAIL AS TMD WITH(NOLOCK)
		ON TME.SYSTEM_ID = TMD.SYSTEM_ID 
	   AND TME.SEQ = TMD.SEQ 
 WHERE TME.DELETE_FLG = 'false' 
   AND TME.FIRST_MANIFEST_KBN = 'true' 
   --AND TME.SYSTEM_ID = /*NEXT_SYSTEM_ID*/
   AND TMD.DETAIL_SYSTEM_ID = /*NEXT_SYSTEM_ID*/
 GROUP BY
   TME.SYSTEM_ID
   ,TMD.DETAIL_SYSTEM_ID
   ,TME.SEQ
   ,TMD.LAST_SBN_GYOUSHA_CD
   ,TMD.LAST_SBN_GENBA_CD
 ORDER BY 
   SEQ DESC