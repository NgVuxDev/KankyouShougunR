--電子現場検索
SELECT MDG.EDI_MEMBER_ID
      ,MDG.GENBA_CD
      ,MDG.JIGYOUJOU_NAME
      ,MDG.JIGYOUJOU_POST
      ,MDG.JIGYOUJOU_TEL
      ,(ISNULL(MDG.JIGYOUJOU_ADDRESS1, '') + ISNULL(MDG.JIGYOUJOU_ADDRESS2, '') + ISNULL(MDG.JIGYOUJOU_ADDRESS3, '') + ISNULL(MDG.JIGYOUJOU_ADDRESS4, '')) AS JIGYOUJOU_ADDRESS
      ,MDG.JIGYOUJOU_CD
      ,MDG.JIGYOUJOU_KBN
      ,MDG.JIGYOUSHA_KBN
      ,MG.GENBA_CD AS MST_GENBA_CD
  FROM M_DENSHI_JIGYOUJOU MDG
  LEFT JOIN M_GENBA MG
    ON MDG.GYOUSHA_CD = MG.GYOUSHA_CD
   AND MDG.GENBA_CD = MG.GENBA_CD
 WHERE MDG.EDI_MEMBER_ID IN/*data.EDI_MEMBER_IDAry*/('kari')