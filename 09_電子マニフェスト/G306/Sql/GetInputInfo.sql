SELECT 
DT_R18.HIKIWATASHI_DATE AS UNNPANN_SYURYOUHI,  --運搬終了日
DT_R18.HAIKI_SUU AS UNNPANN_RYOU,  --運搬量
DT_R18.HAIKI_UNIT_CODE AS UNNPANNRYOU_TANNI_CD, --運搬量単位コード
M_UNIT.UNIT_NAME_RYAKU AS UNNPANNRYOU_TANNI_NAME, --運搬量単位名称
DT_R19.UPN_TAN_NAME AS UNNPANN_TANNTOUSYA_NAME,--運搬担当者名
'' AS SYARYOU_CD,--車輌番号
DT_R19.CAR_NO AS SYARYOU_NAME--車輌名称
FROM
DT_R18
INNER JOIN DT_R19
ON  DT_R18.KANRI_ID = DT_R19.KANRI_ID
AND DT_R18.SEQ = DT_R19.SEQ
AND DT_R19.UPN_ROUTE_NO = /*data.UPN_ROUTE_NO*/
INNER JOIN DT_MF_TOC 
ON DT_R18.KANRI_ID = DT_MF_TOC.KANRI_ID
AND DT_R18.SEQ = DT_MF_TOC.LATEST_SEQ
LEFT JOIN M_UNIT
ON DT_R18.HAIKI_UNIT_CODE = M_UNIT.UNIT_CD
WHERE
DT_R18.KANRI_ID = /*data.KANRI_ID*/ AND
DT_R18.SEQ = /*data.SEQ*/