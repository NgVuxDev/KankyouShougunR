--1次マニフェスト情報の取得
SELECT CASE
           WHEN DT_R08.MEDIA_TYPE IS NULL THEN ''
           WHEN DT_R08.MEDIA_TYPE = 1 THEN '電子'
           WHEN DT_R08.MEDIA_TYPE = 2 THEN '紙'
           ELSE ''
       END AS MEDIA_TYPE,
       CASE
		   WHEN DT_R08.MEDIA_TYPE = 2 THEN
		       CASE WHEN DT_R08.RENRAKU_ID IS NULL OR DT_R08.RENRAKU_ID = ''  THEN ''
			   ELSE DT_R08.RENRAKU_ID END 
           ELSE
		       CASE WHEN DT_R08.FIRST_MANIFEST_ID IS NULL OR DT_R08.FIRST_MANIFEST_ID = '' THEN ''
			   ELSE DT_R08.FIRST_MANIFEST_ID END
		   END AS FIRST_MANIFEST_ID
FROM DT_MF_TOC
LEFT JOIN DT_R08 ON DT_MF_TOC.KANRI_ID = DT_R08.KANRI_ID
                 AND DT_MF_TOC.LATEST_SEQ = DT_R08.SEQ
WHERE (DT_MF_TOC.KIND = 4 OR DT_MF_TOC.KIND = 5 OR DT_MF_TOC.KIND IS NULL)
  AND DT_MF_TOC.KANRI_ID = /*data.kanriId*/
  AND DT_MF_TOC.STATUS_FLAG <> 9
ORDER BY DT_R08.REC_SEQ
