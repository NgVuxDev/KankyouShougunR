SELECT
     TUD.GYOUSHA_NAME_RYAKU AS UNPAN_GYOUSHA_NAME
    ,ISNULL(TUE.SHASHU_NAME, '') AS SHASHU_NAME
	,TUE.SHARYOU_NAME 
	,ISNULL(TUE.UNPAN_GYOUSHA_CD, '') + '：' + ISNULL(TUD.GYOUSHA_NAME_RYAKU, '') + '：' + 
	 ISNULL(TUE.SHASHU_CD, '')        + '：' + ISNULL(TUE.SHASHU_NAME, '')        + '：' +
	 ISNULL(TUE.SHARYOU_CD, '')       + '：' +ISNULL(TUE.SHARYOU_NAME, '')  AS TMP
FROM T_UKETSUKE_SK_ENTRY TUE 
    LEFT JOIN M_GYOUSHA TUD ON TUD.GYOUSHA_CD = TUE.UNPAN_GYOUSHA_CD
WHERE 
    1 = 1 
/*IF unpanGyoushaCd != null*/AND TUE.UNPAN_GYOUSHA_CD = /*unpanGyoushaCd*//*END*/
/*IF sharyouCd != null*/AND TUE.SHARYOU_CD = /*sharyouCd*//*END*/
/*IF shashuCd != null*/AND TUE.SHASHU_CD = /*shashuCd*//*END*/
/*IF gyoushaCd != null*/AND TUE.GYOUSHA_CD = /*gyoushaCd*//*END*/
/*IF genbaCd != null*/AND TUE.GENBA_CD = /*genbaCd*//*END*/
    AND TUE.SAGYOU_DATE >= CONVERT(DATETIME ,/*dateFrom*/,120)
    AND TUE.SAGYOU_DATE <= CONVERT(DATETIME ,/*dateTo*/,120)
    AND TUE.DELETE_FLG = 0
    AND (TUE.SHARYOU_CD IS NOT NULL  OR TUE.UNPAN_GYOUSHA_CD IS NOT NULL  OR TUE.SHASHU_CD IS NOT NULL ) 
GROUP BY 
	TUE.UNPAN_GYOUSHA_CD 
   ,TUE.SHARYOU_CD 
   ,TUE.SHASHU_CD 
   ,TUD.GYOUSHA_NAME_RYAKU
   ,TUE.SHASHU_NAME
   ,TUE.SHARYOU_NAME 