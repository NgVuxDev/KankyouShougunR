SELECT
'' AS SecchiChouhuku,
M_CONTENA.CONTENA_SHURUI_CD,
M_CONTENA_SHURUI.CONTENA_SHURUI_NAME_RYAKU,
M_CONTENA.CONTENA_CD,
M_CONTENA.CONTENA_NAME_RYAKU,
M_CONTENA.GYOUSHA_CD,
M_GYOUSHA.GYOUSHA_NAME_RYAKU,
M_CONTENA.GENBA_CD,
M_GENBA.GENBA_NAME_RYAKU,
M_GENBA.EIGYOU_TANTOU_CD,
M_SHAIN.SHAIN_NAME_RYAKU,
CONVERT(VARCHAR,M_CONTENA.SECCHI_DATE,111) AS SECCHI_DATE,
DATEDIFF(day, M_CONTENA.SECCHI_DATE, GETDATE()) AS DAYSCOUNT,
/*BEGIN*/
 /*IF !data.SYS_DAYS_COUNT.IsNull*/DATEDIFF(day, M_CONTENA.SECCHI_DATE, GETDATE()) / CONVERT(float,/*data.SYS_DAYS_COUNT*/) * 100 AS GRAPH/*END*/
FROM
M_CONTENA
INNER JOIN M_GYOUSHA
ON M_CONTENA.GYOUSHA_CD = M_GYOUSHA.GYOUSHA_CD 
INNER JOIN M_GENBA
ON M_CONTENA.GYOUSHA_CD = M_GENBA.GYOUSHA_CD AND M_CONTENA.GENBA_CD = M_GENBA.GENBA_CD
LEFT JOIN M_SHAIN
ON M_GENBA.EIGYOU_TANTOU_CD = M_SHAIN.SHAIN_CD 
INNER JOIN M_CONTENA_SHURUI
ON M_CONTENA.CONTENA_SHURUI_CD = M_CONTENA_SHURUI.CONTENA_SHURUI_CD
WHERE 
 M_CONTENA.HIKIAGE_DATE IS NULL
 /*IF data.GYOUSHA_CD != null*/AND M_CONTENA.GYOUSHA_CD = /*data.GYOUSHA_CD*//*END*/
 /*IF data.CONTENA_SHURUI_CD != null*/AND M_CONTENA.CONTENA_SHURUI_CD = /*data.CONTENA_SHURUI_CD*//*END*/
 /*IF data.GENBA_CD != null*/AND M_CONTENA.GENBA_CD = /*data.GENBA_CD*//*END*/
 /*IF data.CONTENA_CD != null*/AND M_CONTENA.CONTENA_CD = /*data.CONTENA_CD*//*END*/
 /*IF data.EIGYOU_TANTOU_CD != null*/AND M_GENBA.EIGYOU_TANTOU_CD = /*data.EIGYOU_TANTOU_CD*//*END*/
 /*IF data.ELAPSED_DAYS != null*/AND DATEDIFF(day, M_CONTENA.SECCHI_DATE, GETDATE()) >= /*data.ELAPSED_DAYS*//*END*/
 /*END*/