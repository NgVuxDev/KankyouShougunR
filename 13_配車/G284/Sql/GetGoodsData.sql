SELECT
M_HINMEI.SHURUI_CD,
M_SHURUI.SHURUI_NAME_RYAKU,
M_HINMEI.BUNRUI_CD,
M_BUNRUI.BUNRUI_NAME_RYAKU,
M_HINMEI.HINMEI_CD,
M_HINMEI.HINMEI_NAME,
M_HINMEI.HINMEI_NAME_RYAKU,
M_HINMEI.HINMEI_FURIGANA
FROM M_HINMEI  LEFT JOIN M_SHURUI ON  M_HINMEI.SHURUI_CD = M_SHURUI .SHURUI_CD
               LEFT JOIN M_BUNRUI ON  M_HINMEI.BUNRUI_CD = M_BUNRUI.BUNRUI_CD
WHERE M_HINMEI.DELETE_FLG = 0

