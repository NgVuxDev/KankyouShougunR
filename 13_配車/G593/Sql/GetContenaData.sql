SELECT
	M_CONTENA.CONTENA_SHURUI_CD,
	M_CONTENA_SHURUI.CONTENA_SHURUI_NAME_RYAKU,
	M_CONTENA.CONTENA_CD,
	M_CONTENA.CONTENA_NAME_RYAKU
FROM
	M_CONTENA
	LEFT JOIN M_CONTENA_SHURUI
	ON M_CONTENA.CONTENA_SHURUI_CD = M_CONTENA_SHURUI.CONTENA_SHURUI_CD
WHERE
	M_CONTENA.DELETE_FLG = 0 AND M_CONTENA_SHURUI.DELETE_FLG = 0 
	AND (M_CONTENA.SECCHI_DATE IS NULL OR (M_CONTENA.SECCHI_DATE <= M_CONTENA.HIKIAGE_DATE))
	/*IF data.contenaChuruiCdStart != null*/ AND M_CONTENA_SHURUI.CONTENA_SHURUI_CD >= /*data.contenaChuruiCdStart*/ /*END*/
	/*IF data.contenaChuruiCdEnd != null*/ AND M_CONTENA_SHURUI.CONTENA_SHURUI_CD <= /*data.contenaChuruiCdEnd*/ /*END*/