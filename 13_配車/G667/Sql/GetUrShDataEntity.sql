
SELECT  RT_HANNYUU.HANYU_SEQ_NO
       ,TUSE.KYOTEN_CD
       ,RT.HAISHA_SAGYOU_DATE
       ,TUSE.TORIHIKISAKI_CD
       ,TUSE.TORIHIKISAKI_NAME
       ,RT.GENBA_JISSEKI_GYOUSHACD
       ,TUSE.GYOUSHA_NAME
       ,RT.GENBA_JISSEKI_GENBACD
       ,TUSE.GENBA_NAME
       ,TUSE.EIGYOU_TANTOUSHA_CD
       ,TUSE.EIGYOU_TANTOUSHA_NAME
       ,RT_HANNYUU.HANNYUU_JISSEKI_GYOUSHACD
       ,M_GYOUSHA2.GYOUSHA_NAME_RYAKU AS NIOROSHI_GYOUSHA_NAME
       ,RT_HANNYUU.HANNYUU_JISSEKI_GENBACD
       ,M_GENBA2.GENBA_NAME_RYAKU AS NIOROSHI_GENBA_NAME
       ,RT.SHARYOU_CD
       ,RT.SHARYOU_NAME
       ,RT.SHASHU_CD
       ,RT.SHASHU_NAME
       ,RT.GENBA_JISSEKI_UPNGYOSHACD
       ,TUSE.UNPAN_GYOUSHA_NAME
       ,RT.UNTENSHA_CD
       ,RT.UNTENSHA_NAME
       ,TUSE.MANIFEST_SHURUI_CD
       ,TUSE.MANIFEST_TEHAI_CD
       ,RT.HAISHA_DENPYOU_NO
       ,M_TORIHIKISAKI_SEIKYUU.ZEI_KEISAN_KBN_CD AS SEIKYUU_ZEI_KEISAN_KBN_CD
       ,M_TORIHIKISAKI_SEIKYUU.ZEI_KBN_CD AS SEIKYUU_ZEI_KBN_CD
       ,M_TORIHIKISAKI_SEIKYUU.TORIHIKI_KBN_CD AS SEIKYUU_TORIHIKI_KBN_CD
       ,M_TORIHIKISAKI_SHIHARAI.ZEI_KEISAN_KBN_CD AS SHIHARAI_ZEI_KEISAN_KBN_CD
       ,M_TORIHIKISAKI_SHIHARAI.ZEI_KBN_CD AS SHIHARAI_ZEI_KBN_CD
       ,M_TORIHIKISAKI_SHIHARAI.TORIHIKI_KBN_CD AS SHIHARAI_TORIHIKI_KBN_CD
       ,RT_DTL.GENBA_DETAIL_HINMEICD
       ,M_KOBETSU_HINMEI.SEIKYUU_HINMEI_NAME
       ,M_HINMEI.HINMEI_NAME_RYAKU
       ,RT_DTL.GENBA_DETAIL_SUURYO1
       ,RT_DTL.GENBA_DETAIL_UNIT_CD1
       ,M_HINMEI.ZEI_KBN_CD
       ,RT_DTL.GENBA_DETAIL_SUURYO2
       ,RT_DTL.GENBA_DETAIL_UNIT_CD2
       ,TUSE.SYSTEM_ID AS SYSTEM_ID_TUSE
       ,TUSE.SEQ AS SEQ_TUSE
	   ,RT_DTL.EDABAN AS DTL_EDABAN
  FROM T_MOBISYO_RT RT
  LEFT JOIN T_MOBISYO_RT_DTL RT_DTL ON RT.SEQ_NO = RT_DTL.SEQ_NO AND RT_DTL.DELETE_FLG = 0
  LEFT JOIN T_MOBISYO_RT_HANNYUU RT_HANNYUU ON RT_DTL.HANYU_JISSEKI_SEQ_NO = RT_HANNYUU.HANYU_SEQ_NO AND RT_HANNYUU.DELETE_FLG = 0
  /*IF UKETSUKE_KBN == '1'*/ 
   LEFT JOIN T_UKETSUKE_SS_ENTRY TUSE ON RT.HAISHA_DENPYOU_NO = TUSE.UKETSUKE_NUMBER AND TUSE.DELETE_FLG = 0
  /*END*/
  /*IF UKETSUKE_KBN == '2'*/ 
   LEFT JOIN T_UKETSUKE_SK_ENTRY TUSE ON RT.HAISHA_DENPYOU_NO = TUSE.UKETSUKE_NUMBER AND TUSE.DELETE_FLG = 0
  /*END*/ 
  LEFT JOIN M_TORIHIKISAKI ON TUSE.TORIHIKISAKI_CD = M_TORIHIKISAKI.TORIHIKISAKI_CD
  LEFT JOIN M_TORIHIKISAKI_SEIKYUU ON TUSE.TORIHIKISAKI_CD = M_TORIHIKISAKI_SEIKYUU.TORIHIKISAKI_CD
  LEFT JOIN M_TORIHIKISAKI_SHIHARAI ON TUSE.TORIHIKISAKI_CD = M_TORIHIKISAKI_SHIHARAI.TORIHIKISAKI_CD
  LEFT JOIN M_GYOUSHA ON RT.GENBA_JISSEKI_GYOUSHACD = M_GYOUSHA.GYOUSHA_CD
  LEFT JOIN M_GENBA ON  RT.GENBA_JISSEKI_GYOUSHACD = M_GENBA.GYOUSHA_CD AND RT.GENBA_JISSEKI_GENBACD = M_GENBA.GENBA_CD
  LEFT JOIN M_GYOUSHA M_GYOUSHA2 ON RT_HANNYUU.HANNYUU_JISSEKI_GYOUSHACD = M_GYOUSHA2.GYOUSHA_CD
  LEFT JOIN M_GENBA M_GENBA2 ON  RT_HANNYUU.HANNYUU_JISSEKI_GYOUSHACD = M_GENBA2.GYOUSHA_CD AND RT_HANNYUU.HANNYUU_JISSEKI_GENBACD = M_GENBA2.GENBA_CD
  LEFT JOIN M_GYOUSHA M_GYOUSHA3 ON RT.GENBA_JISSEKI_UPNGYOSHACD = M_GYOUSHA3.GYOUSHA_CD
  LEFT JOIN M_KOBETSU_HINMEI ON RT.GENBA_JISSEKI_GYOUSHACD = M_KOBETSU_HINMEI.GYOUSHA_CD AND RT.GENBA_JISSEKI_GENBACD = M_KOBETSU_HINMEI.GENBA_CD AND RT_DTL.GENBA_DETAIL_HINMEICD = M_KOBETSU_HINMEI.HINMEI_CD
  LEFT JOIN M_HINMEI ON RT_DTL.GENBA_DETAIL_HINMEICD = M_HINMEI.HINMEI_CD
 WHERE RT.DELETE_FLG = 0
   AND RT.HAISHA_KBN = 1
   AND RT.HAISHA_DENPYOU_NO = /*HAISHA_DENPYOU_NO*/
   AND ((RT_DTL.GENBA_DETAIL_SUURYO1 IS NOT NULL AND RT_DTL.GENBA_DETAIL_SUURYO1 != '') OR (RT_DTL.GENBA_DETAIL_SUURYO2 IS NOT NULL AND RT_DTL.GENBA_DETAIL_SUURYO2 != ''))
ORDER BY 
   RT_DTL.SEQ_NO
  ,RT_DTL.EDABAN
  ,RT_HANNYUU.HANYU_SEQ_NO
   