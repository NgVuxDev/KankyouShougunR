SELECT RT_HAN.HANNYUU_JISSEKI_GYOUSHACD
      ,HAN_GYO.GYOUSHA_NAME_RYAKU AS HANNYUU_JISSEKI_GYOUSHA_NM
      ,RT_HAN.HANNYUU_JISSEKI_GENBACD
      ,HAN_GEN.GENBA_NAME_RYAKU AS HANNYUU_JISSEKI_GENBA_NM
      ,RT_HAN.HANNYUU_JISSEKI_RYO
      ,'kg' AS HANNYUU_JISSEKI_UNIT_NAME
      ,CONVERT(VARCHAR(12), RT_HAN.HANNYUU_HANNYUUDATE, 111) + ' '+CONVERT (VARCHAR,(DATEPART (hour, RT_HAN.HANNYUU_HANNYUUDATE))) + ':' + RIGHT('00'+CONVERT (VARCHAR,(DATEPART (minute, RT_HAN.HANNYUU_HANNYUUDATE))),2) AS HANNYUU_HANNYUUDATE
  FROM T_MOBISYO_RT_HANNYUU RT_HAN
  LEFT JOIN M_GYOUSHA HAN_GYO ON RT_HAN.HANNYUU_JISSEKI_GYOUSHACD = HAN_GYO.GYOUSHA_CD
  LEFT JOIN M_GENBA HAN_GEN ON RT_HAN.HANNYUU_JISSEKI_GYOUSHACD = HAN_GEN.GYOUSHA_CD AND RT_HAN.HANNYUU_JISSEKI_GENBACD = HAN_GEN.GENBA_CD
  INNER JOIN (SELECT RT_DTL.HANYU_JISSEKI_SEQ_NO
                FROM T_MOBISYO_RT_DTL RT_DTL
                INNER JOIN T_MOBISYO_RT RT
                   ON RT.SEQ_NO = RT_DTL.SEQ_NO
                  AND RT.GENBA_JISSEKI_JYOGAIFLG = 0
				  AND RT.DELETE_FLG = 0
                  AND RT.HAISHA_DENPYOU_NO = /*haishaDenpyouNo*/0
                  AND RT.HAISHA_KBN = /*haishaKbn*/0
                WHERE RT_DTL.DELETE_FLG = 0
              GROUP BY
                    RT_DTL.HANYU_JISSEKI_SEQ_NO
                    ) HANYU_DATA ON RT_HAN.HANYU_SEQ_NO = HANYU_DATA.HANYU_JISSEKI_SEQ_NO
WHERE RT_HAN.DELETE_FLG = 0