SELECT HEADER.SYSTEM_ID
      ,HEADER.SEQ
      ,DETAIL.DETAIL_SYSTEM_ID
      ,DETAIL.ROW_NO
      ,HEADER.KYOTEN_CD
      ,HEADER.DENSHU_KBN_CD
      ,DENSHU.DENSHU_KBN_NAME_RYAKU AS DENSHU_KBN_NAME
      ,HEADER.RENKEI_NUMBER
      ,HEADER.DENPYOU_DATE
      ,HEADER.DENPYOU_NUMBER
      ,HEADER.UNPAN_GYOUSHA_CD
      ,HEADER.UNPAN_GYOUSHA_NAME
      ,HEADER.SHARYOU_CD
      ,HEADER.SHARYOU_NAME
      ,HEADER.SHASHU_CD
      ,HEADER.SHASHU_NAME
      ,HEADER.UNTENSHA_CD
      ,HEADER.UNTENSHA_NAME
      ,HEADER.KEITAI_KBN_CD
      ,HEADER.KEITAI_KBN_NAME
      ,HEADER.NIZUMI_GYOUSHA_CD
      ,HEADER.NIZUMI_GYOUSHA_NAME
      ,HEADER.NIZUMI_GENBA_CD
      ,HEADER.NIZUMI_GENBA_NAME
      ,HEADER.NIOROSHI_GYOUSHA_CD
      ,HEADER.NIOROSHI_GYOUSHA_NAME
      ,HEADER.NIOROSHI_GENBA_CD
      ,HEADER.NIOROSHI_GENBA_NAME
      ,DETAIL.UNCHIN_HINMEI_CD
      ,DETAIL.UNCHIN_HINMEI_NAME
      ,DETAIL.NET_JYUURYOU
      ,DETAIL.SUURYOU
      ,DETAIL.UNIT_CD
      ,UNIT.UNIT_NAME_RYAKU
      ,'' AS SUURYOU_UNIT
      ,DETAIL.TANKA
      ,DETAIL.KINGAKU
      ,DETAIL.MEISAI_BIKOU
  FROM T_UNCHIN_ENTRY HEADER
  LEFT JOIN T_UNCHIN_DETAIL DETAIL
    ON DETAIL.SYSTEM_ID = HEADER.SYSTEM_ID
   AND DETAIL.SEQ = HEADER.SEQ
  LEFT JOIN M_DENSHU_KBN DENSHU
    ON DENSHU.DENSHU_KBN_CD = HEADER.DENSHU_KBN_CD
  LEFT JOIN M_UNIT UNIT
    ON UNIT.UNIT_CD = DETAIL.UNIT_CD
 WHERE HEADER.DELETE_FLG = 0
   AND HEADER.DENPYOU_DATE BETWEEN /*data.DENPYOU_DATE_FROM*/'' AND /*data.DENPYOU_DATE_TO*/''
   /*IF data.UNPAN_GYOUSHA_CD_FROM != null*/
   AND HEADER.UNPAN_GYOUSHA_CD >= /*data.UNPAN_GYOUSHA_CD_FROM*/''
   /*END*/
   /*IF data.UNPAN_GYOUSHA_CD_TO != null*/
   AND HEADER.UNPAN_GYOUSHA_CD <= /*data.UNPAN_GYOUSHA_CD_TO*/''
   /*END*/
 ORDER BY HEADER.UNPAN_GYOUSHA_CD, HEADER.DENPYOU_DATE, HEADER.DENPYOU_NUMBER, DETAIL.ROW_NO