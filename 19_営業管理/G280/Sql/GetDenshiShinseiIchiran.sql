                            T_DENSHI_SHINSEI_DETAIL_ACTION
                        ON  T_DENSHI_SHINSEI_DETAIL.DETAIL_SYSTEM_ID = T_DENSHI_SHINSEI_DETAIL_ACTION.DETAIL_SYSTEM_ID
                        AND T_DENSHI_SHINSEI_DETAIL_ACTION.DELETE_FLG = 0
