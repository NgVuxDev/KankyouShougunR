    TDSE.DELETE_FLG = 0
/*IF dto.ShinseiKbn1Cd == 1 && dto.ShinseiKbn2Cd == 1*/
    AND TDSD.SHAIN_CD = /*dto.ShainCd*/'000001'
/*END*/
/*IF dto.ShinseiKbn1Cd == 1 && dto.ShinseiKbn2Cd == 3*/
    AND TDSS.SHINSEI_STATUS_CD = 1
/*END*/
/*IF dto.ShinseiKbn1Cd == 2*/
    AND TDSE.SHINSEISHA_CD = /*dto.ShainCd*/'000001'
    AND TDSS.SHINSEI_STATUS_CD = /*dto.ShinseiStatus*/0
/*END*/
/*IF !dto.ShinseiDateFrom.IsNull*/
    AND TDSE.SHINSEI_DATE >= CONVERT(datetime, /*dto.ShinseiDateFrom.Value*/'2014/01/01')
/*END*/
/*IF !dto.ShinseiDateTo.IsNull*/
    AND TDSE.SHINSEI_DATE <= CONVERT(datetime, /*dto.ShinseiDateTo.Value*/'2014/12/31')
/*END*/
/*IF dto.KyotenCd != null && dto.KyotenCd != '' && dto.KyotenCd != '99'*/
    AND TDSE.KYOTEN_CD = CONVERT(int, /*dto.KyotenCd*/0)
/*END*/

/*IF dto.ShinseiKbn1Cd == 1 && dto.ShinseiKbn2Cd == 1*/
UNION ALL
