                TDSD.SEQ,
                TDSD.DETAIL_SYSTEM_ID,
                TDSD.SHINSEI_NUMBER,
                TDSD.ROW_NO,
                TDSD.SHAIN_CD
            FROM
                T_DENSHI_SHINSEI_DETAIL AS TDSD JOIN
