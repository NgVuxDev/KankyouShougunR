                TDSD.SHINSEI_NUMBER,
                TDSD.ROW_NO,
                TDSD.BUSHO_CD,
                TDSD.SHAIN_CD,
                TDSD.DAIRI_KESSAI_SHAIN_CD,
                TDSDA.CHECK_DATE,
                TDSDA.ACTION_FLG,
                TDSDA.ACTION_COMMENT
            FROM
                T_DENSHI_SHINSEI_DETAIL AS TDSD
                LEFT JOIN
                    T_DENSHI_SHINSEI_DETAIL_ACTION AS TDSDA
                ON  TDSD.DETAIL_SYSTEM_ID = TDSDA.DETAIL_SYSTEM_ID
