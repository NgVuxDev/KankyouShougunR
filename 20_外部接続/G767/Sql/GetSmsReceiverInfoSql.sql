SELECT SMSR.*
 FROM M_SMS_RECEIVER SMSR
	LEFT JOIN M_SMS_RECEIVER_LINK_GENBA SMSRG ON SMSR.SYSTEM_ID = SMSRG.SYSTEM_ID
	AND SMSR.MOBILE_PHONE_NUMBER = SMSRG.MOBILE_PHONE_NUMBER
 WHERE SMSR.DELETE_FLG = 0
   AND SMSRG.GYOUSHA_CD = /*data.GYOUSHA_CD*/''
   AND SMSRG.GENBA_CD = /*data.GENBA_CD*/''
 ORDER BY SMSR.SYSTEM_ID ASC