SELECT 
 *  
FROM
(
SELECT 
    M_HIKIAI_GENBA.GENBA_CD
  , M_HIKIAI_GENBA.GENBA_NAME_RYAKU
  , M_HIKIAI_GENBA.GENBA_FURIGANA
  , M_HIKIAI_GENBA.GENBA_POST
  , M_TODOUFUKEN.TODOUFUKEN_NAME_RYAKU
  , M_HIKIAI_GENBA.GENBA_ADDRESS1
  , M_HIKIAI_GENBA.GENBA_TEL
  , CONVERT(bit, '1') AS GENBA_HIKIAI_FLG
  , M_GYOUSHA.GYOUSHA_CD
  , M_GYOUSHA.GYOUSHA_NAME_RYAKU
  , M_GYOUSHA.GYOUSHA_FURIGANA
  , M_GYOUSHA.GYOUSHA_POST
  , M_GYOUSHA.GYOUSHA_ADDRESS1
  , M_GYOUSHA.GYOUSHA_TEL
  , CONVERT(bit, ISNULL(M_HIKIAI_GENBA.HIKIAI_GYOUSHA_USE_FLG, '0')) AS GYOUSHA_HIKIAI_FLG
  , M_GYOUSHA.TEKIYOU_BEGIN
  , M_GYOUSHA.TEKIYOU_END
  , M_GYOUSHA.DELETE_FLG 
  , M_GYOUSHA.DELETE_FLG AS GYOUSHA_DELETE_FLG
  , M_HIKIAI_GENBA.DELETE_FLG AS GENBA_DELETE_FLG
  , M_GYOUSHA.SHOKUCHI_KBN AS GYOUSHA_SHOKUCHI_KBN 
  , M_HIKIAI_GENBA.SHOKUCHI_KBN AS GENBA_SHOKUCHI_KBN 
  , M_HIKIAI_GENBA.MANI_HENSOUSAKI_KBN
  , M_HIKIAI_GENBA.TORIHIKISAKI_CD 
FROM 
  M_HIKIAI_GENBA 
  LEFT JOIN M_TODOUFUKEN 
    ON M_HIKIAI_GENBA.GENBA_TODOUFUKEN_CD = M_TODOUFUKEN.TODOUFUKEN_CD 
    AND M_TODOUFUKEN.DELETE_FLG = 0 
  INNER JOIN M_GYOUSHA 
    ON M_HIKIAI_GENBA.GYOUSHA_CD = M_GYOUSHA.GYOUSHA_CD
  WHERE  ISNULL(M_HIKIAI_GENBA.HIKIAI_GYOUSHA_USE_FLG, 0) = 0 
		 AND M_HIKIAI_GENBA.GENBA_CD_AFTER IS NULL

	UNION  

SELECT 
  M_HIKIAI_GENBA.GENBA_CD
  , M_HIKIAI_GENBA.GENBA_NAME_RYAKU
  , M_HIKIAI_GENBA.GENBA_FURIGANA
  , M_HIKIAI_GENBA.GENBA_POST
  , M_TODOUFUKEN.TODOUFUKEN_NAME_RYAKU
  , M_HIKIAI_GENBA.GENBA_ADDRESS1
  , M_HIKIAI_GENBA.GENBA_TEL
  , '1' AS GENBA_HIKIAI_FLG
  , M_HIKIAI_GYOUSHA.GYOUSHA_CD
  , M_HIKIAI_GYOUSHA.GYOUSHA_NAME_RYAKU
  , M_HIKIAI_GYOUSHA.GYOUSHA_FURIGANA
  , M_HIKIAI_GYOUSHA.GYOUSHA_POST
  , M_HIKIAI_GYOUSHA.GYOUSHA_ADDRESS1
  , M_HIKIAI_GYOUSHA.GYOUSHA_TEL
  , M_HIKIAI_GENBA.HIKIAI_GYOUSHA_USE_FLG AS GYOUSHA_HIKIAI_FLG
  , M_HIKIAI_GYOUSHA.TEKIYOU_BEGIN
  , M_HIKIAI_GYOUSHA.TEKIYOU_END
  , M_HIKIAI_GYOUSHA.DELETE_FLG 
  , M_HIKIAI_GYOUSHA.DELETE_FLG AS GYOUSHA_DELETE_FLG
  , M_HIKIAI_GENBA.DELETE_FLG AS GENBA_DELETE_FLG
  , M_HIKIAI_GYOUSHA.SHOKUCHI_KBN AS GYOUSHA_SHOKUCHI_KBN 
  , M_HIKIAI_GENBA.SHOKUCHI_KBN AS GENBA_SHOKUCHI_KBN 
  , M_HIKIAI_GENBA.MANI_HENSOUSAKI_KBN 
  , M_HIKIAI_GENBA.TORIHIKISAKI_CD 
FROM 
  M_HIKIAI_GENBA 
  LEFT JOIN M_TODOUFUKEN 
    ON M_HIKIAI_GENBA.GENBA_TODOUFUKEN_CD = M_TODOUFUKEN.TODOUFUKEN_CD 
    AND M_TODOUFUKEN.DELETE_FLG = 0 
  INNER JOIN M_HIKIAI_GYOUSHA 
    ON M_HIKIAI_GENBA.GYOUSHA_CD = M_HIKIAI_GYOUSHA.GYOUSHA_CD 
	WHERE M_HIKIAI_GENBA.HIKIAI_GYOUSHA_USE_FLG = 1 
		  AND M_HIKIAI_GENBA.GENBA_CD_AFTER IS NULL
) AS M_HIKIAI_GENBA 