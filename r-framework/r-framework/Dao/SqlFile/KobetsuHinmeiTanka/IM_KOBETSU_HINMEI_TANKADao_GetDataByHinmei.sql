SELECT top 1 * FROM dbo.M_KOBETSU_HINMEI_TANKA
INNER JOIN M_HINMEI ON M_KOBETSU_HINMEI_TANKA.HINMEI_CD = M_HINMEI.HINMEI_CD
AND M_HINMEI.DELETE_FLG = 0
WHERE 
CONVERT(DATE, ISNULL(TEKIYOU_BEGIN, DATEADD(day,-1,GETDATE()))) <= CONVERT(DATE, GETDATE()) and CONVERT(DATE, GETDATE()) <= CONVERT(DATE, ISNULL(TEKIYOU_END, DATEADD(day,1,GETDATE())))
AND M_KOBETSU_HINMEI_TANKA.DELETE_FLG = 0
AND (TORIHIKISAKI_CD = '' OR TORIHIKISAKI_CD = /*data.TORIHIKISAKI_CD*/0)
AND (GYOUSHA_CD = '' OR GYOUSHA_CD = /*data.GYOUSHA_CD*/0)
AND (GENBA_CD = '' OR GENBA_CD = /*data.GENBA_CD*/0)
AND M_KOBETSU_HINMEI_TANKA.HINMEI_CD = /*data.HINMEI_CD*/0
