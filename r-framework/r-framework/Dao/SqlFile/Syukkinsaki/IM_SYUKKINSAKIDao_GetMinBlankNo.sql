SELECT ISNULL(MIN(S.SYUKKINSAKI_CD),1) SYUKKINSAKI_CD
FROM (
		SELECT (SYUKKINSAKI_CD+1) SYUKKINSAKI_CD FROM M_SYUKKINSAKI WHERE ISNUMERIC(SYUKKINSAKI_CD)=1 
		UNION 
		SELECT (TORIHIKISAKI_CD+1) SYUKKINSAKI_CD FROM M_TORIHIKISAKI WHERE ISNUMERIC(TORIHIKISAKI_CD)=1
	) S
WHERE S.SYUKKINSAKI_CD NOT IN (
		SELECT (SYUKKINSAKI_CD+0) SYUKKINSAKI_CD FROM M_SYUKKINSAKI WHERE ISNUMERIC(SYUKKINSAKI_CD)=1
		UNION
		SELECT (TORIHIKISAKI_CD+0) SYUKKINSAKI_CD FROM M_TORIHIKISAKI WHERE ISNUMERIC(TORIHIKISAKI_CD)=1
	)